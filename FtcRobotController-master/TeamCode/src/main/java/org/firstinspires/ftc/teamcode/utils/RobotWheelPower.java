package org.firstinspires.ftc.teamcode.utils;

public class RobotWheelPower {
    public double leftFrontPower;
    public double rightFrontPower;
    public double leftRearPower;
    public double rightRearPower;

    public RobotWheelPower(double _leftFrontPower, double _rightFrontPower, double _leftRearPower, double _rightRearPower) {
        leftFrontPower = _leftFrontPower;
        rightFrontPower = _rightFrontPower;
        leftRearPower = _leftRearPower;
        rightRearPower = _rightRearPower;
    }
}
